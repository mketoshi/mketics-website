export function getServiceByTitle(title) {
  return serviceExplorerItems.find((service) => service.title === title);
}

export function getServicePillarForTitle(title) {
  const pillar = servicePillarGroups.find((group) =>
    group.services.includes(title)
  );

  return pillar?.title || "MKETICS Digital Business Solutions";
}

export function getReadinessLevel(answers = {}) {
  const selectedValues = Object.values(answers);

  if (
    selectedValues.includes("idea") ||
    selectedValues.includes("planning") ||
    selectedValues.includes("not_sure")
  ) {
    return "Discovery stage";
  }

  if (
    selectedValues.includes("starting") ||
    selectedValues.includes("entry") ||
    selectedValues.includes("immediately")
  ) {
    return "Starter readiness";
  }

  if (
    selectedValues.includes("already_running") ||
    selectedValues.includes("standard") ||
    selectedValues.includes("this_month")
  ) {
    return "Business readiness";
  }

  if (
    selectedValues.includes("improving") ||
    selectedValues.includes("growth") ||
    selectedValues.includes("one_three_months")
  ) {
    return "Growth readiness";
  }

  if (
    selectedValues.includes("scaling") ||
    selectedValues.includes("custom")
  ) {
    return "Digital transformation readiness";
  }

  return "Needs assessment recommended";
}

export function getServiceExplorerRecommendation(answers = {}) {
  const scores = new Map();
  const selectedAnswerLabels = [];

  serviceExplorerQuestions.forEach((question) => {
    const selectedOptionId = answers[question.id];
    const selectedOption = question.options.find(
      (option) => option.id === selectedOptionId
    );

    if (!selectedOption) return;

    selectedAnswerLabels.push({
      question: question.question,
      answer: selectedOption.label,
    });

    selectedOption.services.forEach((serviceTitle, index) => {
      const currentScore = scores.get(serviceTitle) || 0;
      const weightedScore = Math.max(1, 6 - index);
      scores.set(serviceTitle, currentScore + weightedScore);
    });
  });

  const rankedTitles = [...scores.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([title]) => title);

  const fallbackTitle = "Free Consultation / Needs Assessment";
  const primaryTitle = rankedTitles[0] || fallbackTitle;

  const primaryService =
    getServiceByTitle(primaryTitle) || getServiceByTitle(fallbackTitle);

  const supportingServices = rankedTitles
    .filter((title) => title !== primaryService?.title)
    .slice(0, 4)
    .map(getServiceByTitle)
    .filter(Boolean);

  const servicePillar = getServicePillarForTitle(primaryService?.title);
  const readinessLevel = getReadinessLevel(answers);

  return {
    primaryService,
    supportingServices,
    servicePillar,
    readinessLevel,
    selectedAnswerLabels,
    suggestedStartingPoint:
      primaryService?.title === "Free Consultation / Needs Assessment"
        ? "Start with a guided consultation so MKETICS can understand your goals, budget direction and best next step."
        : `Start with ${primaryService?.title}, then add supporting services as your needs grow.`,
    whyThisFits:
      "This recommendation is based on your selected business need, client type, current stage, biggest challenge, desired outcome, budget direction and timeline.",
    nextStep:
      "Request a quote or send this recommendation on WhatsApp so MKETICS can confirm scope, timeline and pricing direction.",
  };
}